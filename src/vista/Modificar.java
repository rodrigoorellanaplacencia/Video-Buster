    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Metodos metodo = new Metodos();
        Pelicula pelicula = metodo.buscar(Integer.parseInt(txtCodigo.getText()));
        if(metodo.existePelicula(Integer.parseInt(txtCodigo.getText())))
        {    
            txtNombre.setText(pelicula.getNombre());
            txtPrecio.setText(""+pelicula.getPrecio());
            cbxCategoria.setSelectedIndex(pelicula.getIdCategoria());
            rbSi.setSelected(pelicula.getFormato4k()=='s');
            rbNo.setSelected(pelicula.getFormato4k()=='n');
        }
        else
        {
            JOptionPane.showMessageDialog(new JDialog(), "Código no encontrado.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Metodos metodo = new Metodos();
        char formato4k = 'n';
        if(rbSi.isSelected())
        {
            formato4k = 's';
        }
        
        try {
            metodo.modificar(new Pelicula(Integer.parseInt(txtCodigo.getText()), Integer.parseInt(txtPrecio.getText()), cbxCategoria.getSelectedIndex(), formato4k, txtNombre.getText()), Integer.parseInt(txtCodigo.getText()));
        } catch (SQLException ex) {
            Logger.getLogger(Modificar.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(new JDialog(), "Modificacion realizada");
        
    }//GEN-LAST:event_jButton4ActionPerformed

